package com.zishanfu.vistrips.model;

public class Node {
	public double lat;
	public double lon;
	
	public Node() {};
	
	public Node(double lat, double lon) {
		this.lat = lat;
		this.lon = lon;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public double getLon() {
		return lon;
	}

	public void setLon(double lon) {
		this.lon = lon;
	}

}
